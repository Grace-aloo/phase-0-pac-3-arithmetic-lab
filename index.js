function add(a,b){
    return a+b;
 }
function subtract(a,b){
    return a-b;
}
function multiply(a,b){
    return a*b;
}
function divide(a,b){
    return a/b;
}
function increment(num1){
    num1++;
    return num1;
}
function decrement(num2){
     num2--;
     return num2;
}
